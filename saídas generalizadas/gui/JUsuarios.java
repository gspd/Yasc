    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed
        // TODO add your handling code here:
        String add = JOptionPane.showInputDialog(this, palavras.getString("Enter the name"), palavras.getString("Add"), JOptionPane.QUESTION_MESSAGE);
        if(!usuarios.contains(add) && !add.equals("")){
            usuarios.add(add);
            jListUsers.setListData(usuarios);
        }
    }//GEN-LAST:event_jButtonAdicionarActionPerformed

    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed
        // TODO add your handling code here:
        if (!jListUsers.isSelectionEmpty()) {
            String aux = this.jListUsers.getSelectedValue().toString();
            int escolha = JOptionPane.showConfirmDialog(this, palavras.getString("Are you sure want delete this user:") + aux, null, JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (escolha == JOptionPane.YES_OPTION) {
                usuarios.remove(aux);
                jListUsers.setListData(usuarios);
            }
        } else {
            JOptionPane.showMessageDialog(this, palavras.getString("A user should be selected"));
        }
    }//GEN-LAST:event_jButtonRemoverActionPerformed

