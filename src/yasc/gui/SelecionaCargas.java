    /**
     * Carrega os valores do gerador de cargas nos componentes da janela
     *
     * @param gerarcarga
     */
    private void setValores(GerarCarga gerarcarga) {
        if (gerarcarga != null) {
            switch (gerarcarga.getTipo()) {
                case GerarCarga.RANDOM:
                    CargaRandom random = (CargaRandom) gerarcarga;
                    jSpinnerNumTarefas.setValue(random.getNumeroTarefas());
                    jSpinnerMed.setValue(random.getAvg2());
                    jSpinnerMax.setValue(random.getMax());
                    jSpinnerMin.setValue(random.getMin());
                    jSpinnerAvgPoisson.setValue(random.getAvgPoisson());
                    jSpinnerDesvPadNormal.setValue(random.getDesvPad());
                    jSpinnerAverageNormal.setValue(random.getAvgNormal());
                    jSpinnerTimeArrival.setValue(random.getArrivalTime());
                    jCheckboxDistribuirIgual.setSelected(random.isDistribuirIgual());
                    setTipo(GerarCarga.RANDOM);
                    break;
                case GerarCarga.UNIFORM:
                    CargaConst _carga = (CargaConst) gerarcarga;
                    jSpinnerNumTarefasNo.setValue(_carga.getNumTars());
                    jSpinnerTam.setValue(_carga.getTam());
                    jSpinnerTimeArrival1.setValue(_carga.getArrivalTime());
                    jCheckBoxDistribuirIgual2.setSelected(_carga.isDistribuirIgual());
                    setTipo(GerarCarga.UNIFORM);
                    break;
            }
        } else {
            setTipo(GerarCarga.RANDOM);
            jRadioButton2Stage.setSelected(true);
            jRadioButtonPoisson.setSelected(false);
            jLabel2.setEnabled(false);
            jSpinnerAvgPoisson.setEnabled(false);
            jRadioButtonNormal.setSelected(false);
            jLabel3.setEnabled(false);
            jLabel8.setEnabled(false);
            jSpinnerAverageNormal.setEnabled(false);
            jSpinnerDesvPadNormal.setEnabled(false);
        }
    }

    public HashSet<String> getUsuarios() {
        return new HashSet<>(usuarios);
    }

    /**
     * Apresenta na janela jPanel e seta valores dos jRadioButton de acordo com
     * tipo informado
     *
     * @param tipo jRadioButton selecionado
     */
    private void setTipo(int tipo) {
        switch (tipo) {
            case GerarCarga.RANDOM:
                jRadioButtonUniform.setSelected(false);
                jRadioButtonRandom.setSelected(true);
                jScrollPaneSelecionado.setViewportView(jPanelRandom);
                break;
            case GerarCarga.UNIFORM:
                jRadioButtonUniform.setSelected(true);
                jRadioButtonRandom.setSelected(false);
                jScrollPaneSelecionado.setViewportView(jPanelUniform);
                break;
        }
    }
