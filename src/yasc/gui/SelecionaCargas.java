    private void jButtonOK1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOK1ActionPerformed
        // TODO add your handling code here:
        if (jRadioButtonRandom.isSelected()) {
            try {
                int numTaref = (Integer) jSpinnerNumTarefas.getValue();
                double avg = (double) jSpinnerMed.getValue();
                double max = (double) jSpinnerMax.getValue();
                double min = (double) jSpinnerMin.getValue();
                double avgPoisson = Double.parseDouble(jSpinnerAvgPoisson.getValue().toString());
                double avgNormal = Double.parseDouble(jSpinnerAverageNormal.getValue().toString());
                double DesvPad = Double.parseDouble(jSpinnerDesvPadNormal.getValue().toString());
                double ArrTime = Double.parseDouble(jSpinnerTimeArrival.getValue().toString());
                boolean distrIgual = jCheckboxDistribuirIgual.isSelected();
                int TipoDistr = 0;
                if (jRadioButton2Stage.isSelected()) {
                    TipoDistr = 0;
                } else if (jRadioButtonPoisson.isSelected()) {
                    TipoDistr = 1;
                } else if (jRadioButtonNormal.isSelected()) {
                    TipoDistr = 2;
                }
                this.carga = new CargaRandom(numTaref, min, max, avg, avgPoisson, avgNormal, DesvPad, TipoDistr, distrIgual, ArrTime);
            } catch (NumberFormatException ex) {
                Logger.getLogger(SelecionaCargas.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (jRadioButtonUniform.isSelected()) {
            try {
                int numeroTarefas = Integer.parseInt(jSpinnerNumTarefasNo.getValue().toString());
                double Tam = Double.parseDouble(jSpinnerTam.getValue().toString());
                boolean distrIgual = jCheckBoxDistribuirIgual2.isSelected();
                double ArrTime1 = Double.parseDouble(jSpinnerTimeArrival1.getValue().toString());
                this.carga = new CargaConst(numeroTarefas, Tam, distrIgual, ArrTime1);
            } catch (NumberFormatException ex) {
                Logger.getLogger(SelecionaCargas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        this.setVisible(false);
    }//GEN-LAST:event_jButtonOK1ActionPerformed

