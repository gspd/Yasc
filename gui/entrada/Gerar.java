    /**
     * Método para contar parentesis
     *
     * @param s
     * @return Número de parentesis abertos
     */
    public static int verifica(String s) {
        int cont = 0;
        for (int i = 0; i < s.length(); i++) {
            if (s.charAt(i) == '(') {
                cont++;
            }
            if (s.charAt(i) == ')') {
                cont--;
            }
        }
        return cont;
    }

