    private void passo4() {
        parserTable.setFuncaoTrans(jTextFieldP4Formula.getText());

        if (cbxOperation.getSelectedIndex() == 0) {
            parserTable.setLogicOperation("n");
        }

        int val = verifica(parserTable.getFuncaoTrans());
        if (val != 0) {
            if (val >= 0) {
                JFrame frame = new JFrame("Warning");
                JOptionPane.showMessageDialog(frame, "Add ) to correct the expression");
            } else {
                JFrame frame = new JFrame("Warning");
                JOptionPane.showMessageDialog(frame, "Add ( to correct the expression");
            }
        } else {
            if (jRadioButtonP2Instantaneo.isSelected() && !jRadioButton2Table.isSelected()) {
                this.jScrollPanePrincipal.setViewportView(this.jPanelPasso7);
                this.jLabelPasso4.setEnabled(false);
                this.jLabelPasso7.setEnabled(true);
                this.jButtonProximo.setEnabled(false);
                this.jButtonFinalizar.setEnabled(true);
                this.passoAtual = 7;

                escreverFormula();
                escreverGramatica();
            }
        }
        if (jRadioButtonP2Instantaneo.isSelected() && jRadioButton2Table.isSelected()) {
            parserTable.setInputTrans(jTextField2.getText());
            parserTable.setOutputTrans(jTextField3.getText());
            int val1 = verifica(parserTable.getInputTrans());
            int val2 = verifica(parserTable.getOutputTrans());

            if (val1 != 0 || val2 != 0) {
                if (val1 >= 0 || val2 >= 0) {
                    JFrame frame = new JFrame("Warning");
                    JOptionPane.showMessageDialog(frame, "Add ) to correct the expression");
                } else {
                    JFrame frame = new JFrame("Warning");
                    JOptionPane.showMessageDialog(frame, "Add ( to correct the expression");
                }
            } else {
                this.jScrollPanePrincipal.setViewportView(this.jPanelPasso7);
                this.jLabelPasso4.setEnabled(false);
                this.jLabelPasso7.setEnabled(true);
                this.jButtonProximo.setEnabled(false);
                this.jButtonFinalizar.setEnabled(true);
                this.passoAtual = 7;

                escreverInput();
                escreverOutput();
                escreverGramatica();
            }
        }
    }
    
